# Strings in Python #
#===================#

# Print a normal string

print("Wrap a string always within single/double quotes")

# Concatenating strings

print("Hello" + " World !!!")

# Adding numerics within a string

print("This will cost you around " + str(6+5) + " dollars.")

# Splitting a string

spltr = "Hello:Udit".split(":")
print(spltr)

# Utilizing the splitted string

print("Hello " + spltr[1])

# Printing without the type-conversion
print("Hello my age is", 26)
